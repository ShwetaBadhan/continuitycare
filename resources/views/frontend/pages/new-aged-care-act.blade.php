                            <p>
At Southern Cross Care, we are committed to grow communities where best lives are lived – and that means delivering care that is not only safe, kind and effective, but grounded in dignity and choice.
</p>
<p>The Australian Government’s new Aged Care Act and the Strengthened Aged Care Quality Standards are designed to improve outcomes for older Australians. We welcome these reforms, and our services are now aligned with the new requirements.</p>
<p>A key part of these changes is the introduction of the Support at Home program – a reformed home care model designed to help older Australians live with support at home for longer. The program has replaced the Home Care Packages (HCP) and Short-Term Restorative Care (STRC) programs, and the Commonwealth Home Support Programme (CHSP) will transition no earlier than 1 July 2027. These reforms aim to streamline in-home care services, provide fairer access, and deliver more personalised support tailored to individual needs.</p>
<p>We see this as an opportunity to further embed our mission and values in how we serve residents, clients, and families — with a focus on transparency, continuous improvement, and person-centred care.</p>
